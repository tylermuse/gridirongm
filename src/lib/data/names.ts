const usedNames = new Set<string>();

export function randomName(): { firstName: string; lastName: string } {
  // Try to generate a unique full name (up to 10 attempts)
  for (let i = 0; i < 10; i++) {
    const firstName = FIRST_NAMES[Math.floor(Math.random() * FIRST_NAMES.length)];
    const lastName = LAST_NAMES[Math.floor(Math.random() * LAST_NAMES.length)];
    const key = `${firstName} ${lastName}`;
    if (!usedNames.has(key)) {
      usedNames.add(key);
      return { firstName, lastName };
    }
  }
  // Fallback: return without dedup check
  return {
    firstName: FIRST_NAMES[Math.floor(Math.random() * FIRST_NAMES.length)],
    lastName: LAST_NAMES[Math.floor(Math.random() * LAST_NAMES.length)],
  };
}

/** Clear the used-names tracker (call when starting a new league) */
export function resetUsedNames() {
  usedNames.clear();
}
